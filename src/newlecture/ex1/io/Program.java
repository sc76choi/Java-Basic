package newlecture.ex1.io;

public class Program {
    public static void main(String[] args) {
        System.out.write('3');
        System.out.println();
        System.out.write(51);
        System.out.flush();
        System.out.println();
        System.out.write('3'+ 1);
        System.out.write('3'+ 2);
        System.out.write('3'+ 3);
        System.out.write('3'+ 4);
        System.out.flush();
        System.out.println();
        System.out.write('A'+ 1);
        System.out.write('A'+ 2);
        System.out.write('A'+ 3);
        System.out.write('A'+ 4);
        System.out.flush();
    }
}
